// 由 personas/*.md 產生 lib/personas.data.js（serverless function import 用）。
// Vercel 部署時會自動跑（見 vercel.json 嘅 buildCommand）。
// 加/改 persona：改 personas/*.md，然後 `npm run build`。

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const personaDir = join(root, "personas");

const personas = readdirSync(personaDir)
  .filter((f) => f.endsWith(".md"))
  .sort()
  .map((file) => {
    const content = readFileSync(join(personaDir, file), "utf8");
    const id = file.replace(/\.md$/, "");
    const titleMatch = content.match(/^#\s+(.+)$/m);
    const title = titleMatch ? titleMatch[1].trim() : id;
    const descLine = content
      .split("\n")
      .find((l) => l.trim() && !l.startsWith("#") && !l.startsWith(">"));
    const description = descLine ? descLine.trim() : "";
    return { id, title, description, content };
  });

mkdirSync(join(root, "lib"), { recursive: true });
const out =
  "// AUTO-GENERATED by scripts/build-personas.mjs — 唔好手改。\n" +
  "// 改 persona 請改 personas/*.md，再跑 `npm run build`。\n" +
  `export default ${JSON.stringify(personas, null, 2)};\n`;
writeFileSync(join(root, "lib", "personas.data.js"), out);
console.log(`Wrote lib/personas.data.js with ${personas.length} personas.`);
